package CoreJavaPart2;

public class ifElseForLoop {

	public static void main(String[] args) {
		
		if(8==8)
			System.out.println("Success");
		else
			System.out.println("fail");
		
		
		for(int i=0; i<10; i = i+3) 
		{
			if(i==9)
				System.out.println("Success");
			else
				System.out.println("fail");
		}

	}

}
