package CoreJavaPart2;

public class InvertedPyramidTriangle {

	public static void main(String[] args) {
		
		//int k =1;
		for(int i =1; i<=5; i++) 
		{
			for(int j =1; j<=i; j++)
			{
				System.out.print(j);
				System.out.print("\t");
				//k++;
				
			}
			System.out.println("");
		}

	}

}
